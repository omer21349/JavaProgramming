package day03_EscapeSequences;

public class Diamond2 {
    public static void main(String[] args) {
        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  / * \\");
        System.out.println(" / * * \\");
        System.out.println("/ * * * \\");
        System.out.println("---------");
        System.out.println("\\ * * * /");
        System.out.println(" \\ * * /");
        System.out.println("  \\ * /");
        System.out.println("   \\ /");
        System.out.println("    V  ");

        /*
        Create a class called Diamond2.java, and print:

                    ^
                   / \
                  / * \
                 / * * \
                / * * * \
                ---------
                \ * * * /
                 \ * * /
                  \ * /
                   \ /
                    V
         */
    }
}
