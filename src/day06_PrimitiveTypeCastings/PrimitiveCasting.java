package day06_PrimitiveTypeCastings;

public class PrimitiveCasting {
    public static void main(String[] args) {

        short a = 200;
        int b= (int)a;

        System.out.println(b);

        System.out.println("------------------------");


        double d1= 20.5;
        short s1= (short) d1;
        System.out.println(s1);



    }
}
